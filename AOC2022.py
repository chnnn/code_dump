with open('inputday1.txt') as f:
    maxx = []
    curr = 0

    for i in f:
        line = i.strip()
        if line == '': 
            maxx.append(curr)
            if len(maxx) == 4:
                maxx.sort()
                maxx = maxx[1:]
            curr = 0
        else:
            curr += int(line)
            
    print(maxx, sum(maxx)) #67658 200158
    
